package com.it.bean;

public class Country {
    private int id;
    private String c_name;

    public Country() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getC_name() {
        return c_name;
    }

    public void setC_name(String c_name) {
        this.c_name = c_name;
    }

    @Override
    public String toString() {
        return "Country{" +
                "id=" + id +
                ", c_name='" + c_name + '\'' +
                '}';
    }
}
