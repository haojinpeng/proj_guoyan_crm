$(function() {
	
	$("#d8").click(function(){
		window.open("找回密码.html");
	});
		
	$("#d1").click(function() {
		var z1 = $("#z1").val();
		var m1 = $("#m1").val();
		var z2 = $("#z2").val();
		var m2 = $("#m2").val();
		var name = $(" #p1 ").val();
		var pass = $(" #p2 ").val();

		if(name == z1 && pass == m1) {

			$("#cg1").show();
			window.open("index.html");
		} else if(name == z2 && pass == m2) {
			$("#cg1").show();
			window.open("index.html");
		} else {
		      $("#sb1").show()
			  alert("登录失败，请重新登录")
		}

	});
	
	
	

});